function parseCommand(text) {
  text = text.toLowerCase();
  switch (text) {
    case "help":
      helpScene();
      break;
    case "about_me":
      aboutScene();
      break;
    case "education":
      educationScene();
      break;
    case "employment":
      employmentScene();
      break;
    case "projects":
      projectsScene();
      break;
    case "skills":
      skillsScene();
      break;
    case "resume":
      resumeScene();
      break;
    default:
      IOPrint("Command not recognized. Type HELP for commands.", "orange");
  }
}

